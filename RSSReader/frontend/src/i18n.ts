const translations: Record<AppLanguage, typeof zhText> = {
  "zh-Hans": zhText,
  en: enText,
};

export function getAppText(language: AppLanguage) {
  return translations[language];
}

export function normalizeAppLanguage(value: unknown): AppLanguage {
  return value === "en" || value === "zh-Hans" ? value : defaultAppLanguage;
}

export function appLocale(language: AppLanguage) {
  return language === "zh-Hans" ? "zh-CN" : "en";
}

export function formatClockTime(date: Date, language: AppLanguage) {
  return date.toLocaleTimeString(appLocale(language), {
    hour: "2-digit",
    minute: "2-digit",
  });
}
